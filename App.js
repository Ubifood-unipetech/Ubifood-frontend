import { StyleSheet, View } from 'react-native';
import MapView, { Marker } from 'react-native-maps';
import { useEffect, useState } from 'react';
import * as TaskManager from 'expo-task-manager';
import * as Location from "expo-location"


const api = "http://192.168.0.10:8000/";
// const LOCATION_TASK_NAME = 'background-location-task';

const App = () => {
  const [response, setResponse] = useState([]);
  const [loading, setLoading] = useState(true);
  const [location, setLocation] = useState({});

  async function request(){
    let response = await fetch(api + "api/restaurants/");
    let data = await response.json();
    let results = data.results;
    setResponse(results);


    await Location.requestForegroundPermissionsAsync();

    let loc = await Location.getCurrentPositionAsync({accuracy: Location.Accuracy.Highest, maximumAge: 10000, timeout: 5000});
    setLocation(loc);

    setLoading(false);
  }

  useEffect(() => {
      request();
  }, [])
  
  return (

    <View style={styles.container}>
      { !loading &&   <MapView style={styles.map}
        initialRegion={{
          latitude: location.coords.latitude,
          longitude: location.coords.longitude,
          latitudeDelta: 0.0070,
          longitudeDelta: 0.0070,
        }}
        customMapStyle={mapStyle}
        showsUserLocation={true}
        followsUserLocation={true}
        minZoomLevel={18}
        maxZoomLevel={19}
        userLocationUpdateInterval={1000}
        >
    
          { response.map((obj, index) => (<Marker key={index} title={obj.name} description={obj.desc} coordinate={{latitude:obj.coordinates.coordinates[1], longitude:obj.coordinates.coordinates[0]}}></Marker>))}
         
      </MapView>}
     
    </View>
  );
}

